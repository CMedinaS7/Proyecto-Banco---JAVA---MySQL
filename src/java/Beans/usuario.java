package Beans;

public class usuario {
    
private String cliente;
private String tarjeta;
private String cuenta;
private String idcliente;
private String idcuentabancaria;
private String idtarjeta;

    /**
     * @return the cliente
     */
    public String getCliente() {
        return cliente;
    }

    /**
     * @param cliente the cliente to set
     */
    public void setCliente(String cliente) {
        this.cliente = cliente;
    }

    /**
     * @return the tarjeta
     */
    public String getTarjeta() {
        return tarjeta;
    }

    /**
     * @param tarjeta the tarjeta to set
     */
    public void setTarjeta(String tarjeta) {
        this.tarjeta = tarjeta;
    }

    /**
     * @return the cuenta
     */
    public String getCuenta() {
        return cuenta;
    }

    /**
     * @param cuenta the cuenta to set
     */
    public void setCuenta(String cuenta) {
        this.cuenta = cuenta;
    }

    /**
     * @return the idcliente
     */
    public String getIdcliente() {
        return idcliente;
    }

    /**
     * @param idcliente the idcliente to set
     */
    public void setIdcliente(String idcliente) {
        this.idcliente = idcliente;
    }

    /**
     * @return the idcuentabancaria
     */
    public String getIdcuentabancaria() {
        return idcuentabancaria;
    }

    /**
     * @param idcuentabancaria the idcuentabancaria to set
     */
    public void setIdcuentabancaria(String idcuentabancaria) {
        this.idcuentabancaria = idcuentabancaria;
    }

    /**
     * @return the idtarjeta
     */
    public String getIdtarjeta() {
        return idtarjeta;
    }

    /**
     * @param idtarjeta the idtarjeta to set
     */
    public void setIdtarjeta(String idtarjeta) {
        this.idtarjeta = idtarjeta;
    }
}
